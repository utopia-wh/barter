package com.barter.pojo;

public class Goods {
	private int goodsId;
	private String goodsName;
	private String goodsDescription;
	private String goodsImage;
	private int goodsStatus;
	private String goodsReleaseTime;
	private String goodsDegree;
	private String goodsType;
	private String userEmail;
	public int getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(int goodsId) {
		this.goodsId = goodsId;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public String getGoodsDescription() {
		return goodsDescription;
	}
	public void setGoodsDescription(String goodsDescription) {
		this.goodsDescription = goodsDescription;
	}
	public String getGoodsImage() {
		return goodsImage;
	}
	public void setGoodsImage(String goodsImage) {
		this.goodsImage = goodsImage;
	}
	public int getGoodsStatus() {
		return goodsStatus;
	}
	public void setGoodsStatus(int goodsStatus) {
		this.goodsStatus = goodsStatus;
	}
	public String getGoodsReleaseTime() {
		return goodsReleaseTime;
	}
	public void setGoodsReleaseTime(String goodsReleaseTime) {
		this.goodsReleaseTime = goodsReleaseTime;
	}
	public String getGoodsDegree() {
		return goodsDegree;
	}
	public void setGoodsDegree(String goodsDegree) {
		this.goodsDegree = goodsDegree;
	}
	public String getGoodsType() {
		return goodsType;
	}
	public void setGoodsType(String goodsType) {
		this.goodsType = goodsType;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	
	
}
